  cudaMalloc(&global_gpu_mem_output_signal, sizeof(short)*(*signal_length)); CUERR("Allocating GPU memory for short representation of RNA");
  short *cpu_mem_output_signal = (short *) std::malloc(sizeof(short)*(*signal_length)); 
  if(cpu_mem_output_signal == 0){
    std::cerr << "Error allocating CPU memory for short representation of RNA (" << sizeof(short)*(*signal_length) << "bytes), aborting."<< std::endl;
    exit(1);
  }

  char *gpu_mem_rna;
  cudaMalloc(&gpu_mem_rna, sizeof(char)*rna_length); CUERR("Allocating GPU memory for char representation of RNA");
  cudaMemcpy(gpu_mem_rna, cpu_mem_rna, sizeof(char)*rna_length, cudaMemcpyHostToDevice); CUERR("Copying RNA from CPU to GPU");

  rna2signal<<<num_blocks,CUDA_THREADBLOCK_MAX_THREADS>>>(gpu_mem_rna, rna_length, signal_type, strand_types, global_gpu_mem_output_signal); CUERR("Executing RNA to signal conversion on the GPU");

  cudaMemcpy(cpu_mem_output_signal, global_gpu_mem_output_signal, sizeof(short)*(*signal_length), cudaMemcpyDeviceToHost); CUERR("Copying short representation of RNA from GPU to CPU");
  cudaFree(global_gpu_mem_output_signal);

  return cpu_mem_output_signal;
}

